int	ft_is_prime(int nb)
{
	int	i;
	int	div;

	i = 2;
	if (nb == 1)
		return (0);
	while (i < nb)
	{
		div = nb % i;
		if (div == 0)
			return (0);
		i++;
	}
	return (1);
}
